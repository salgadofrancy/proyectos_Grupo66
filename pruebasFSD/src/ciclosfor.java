public class ciclosfor {
    public static void main(String[] args) {
        //ciclo for
        //for (inicializacion; condicion; iteracion-> como se va repitiendo) {}
        // conteo del 10 al 20 de 1 en 1
        for (int i=10; i<=20; i++){
            System.out.println(i);
            }
            System.out.println("------------------------");
        // para incrementar de 2 en 20
        for (int i=10; i<=20; i+=2){
            System.out.println(i);
            }
            System.out.println("------------------------");
        // iniciamos un conteo en reversa, decreciendo de 2 en 2
        for (int i=50; i>=0; i-=2){
            System.out.println(i);
            }
    }
       
}
